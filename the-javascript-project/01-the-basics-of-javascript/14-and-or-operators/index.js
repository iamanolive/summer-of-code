let firstName = "Cassidy";
let age = 120;
// the "and" operator
if (firstName[0] === "C" && age > 18)
    console.log("your name starts with C and you are above 18");
else console.log("you are inside the else block");
// the "or" operator
if (firstName[0] === "C" || age > 18)
    console.log("you are inside the if block");
else console.log("you are inside the else block");