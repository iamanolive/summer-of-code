// comparison operators
let num1 = 5;
let num2 = 7;
// boolean values
console.log(num1 >= num2);
console.log(num1 <= num2);

// the equality operators
num1 = 7; num2 = 7;
num1 = String(num1);
// does not check for data type
console.log(num1 == num2);
// checks for data type AND value
console.log(num1 === num2);

// the inequality operators
console.log(num1 != num2);
console.log(num1 !== num2);