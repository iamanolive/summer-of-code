let fruits = ["apple", "mango", "grapes"];
console.log(fruits);
// the push method
fruits.push("banana");
console.log(fruits);
// the pop method
console.log(fruits.pop());
console.log(fruits);
// the shift method
console.log(fruits.shift());
console.log(fruits);
// the unshift method
fruits.unshift("banana");
fruits.unshift("kiwi");
console.log(fruits);